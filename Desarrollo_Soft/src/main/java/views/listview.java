    public void GenerarDataA() throws SQLException, ClassNotFoundException{
        String[] valores = new String[4];
        List<Estudio> x = ad.listAll(fi.getId());
        for(Estudio m : x){
            valores[0]=String.valueOf(m.getId());
            valores[1]=m.getUniversidad();
            valores[2]=m.getNivelEstudio();
            valores[3]= m.getTitulo();
            
            tb2.addRow(valores);
        }
    }
    
    
