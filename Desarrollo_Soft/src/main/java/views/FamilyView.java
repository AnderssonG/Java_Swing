    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        if (name.equals("") || rol.equals("") || date.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Faltan Atributos");
        }else{
            fm.setFuncionarioId(id);
            fm.setFechaNacimiento(date.getText());
            fm.setNombreFamiliar(name.getText());
            fm.setRol(rol.getText());
            
            try {
                fd.create(fm);
            } catch (SQLException ex) {
                Logger.getLogger(FamilyView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FamilyView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_createActionPerformed

    private void UpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpActionPerformed
        if (name.equals("") || rol.equals("") || date.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Faltan Atributos");
        }else{
            fm2.setFechaNacimiento((String)date.getText());
            fm2.setNombreFamiliar(name.getText());
            fm2.setRol(rol.getText());
        
            try {
                fd.update(fm2);
            } catch (SQLException ex) {
                Logger.getLogger(FamilyView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FamilyView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_UpActionPerformed

    private void DelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelActionPerformed
        try {
            fd.delete(fm2.getId());
        } catch (SQLException ex) {
            Logger.getLogger(FamilyView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FamilyView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_DelActionPerformed

