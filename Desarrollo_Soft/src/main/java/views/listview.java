            int fila = familiares.rowAtPoint(evt.getPoint());
            int idF = Integer.parseInt((String) tb.getValueAt(fila, 0));
            
            GrupoFamiliar xy =n.getById(idF);
            fv = new FamilyView(xy, listview.this);
            fv.setVisible(true);
