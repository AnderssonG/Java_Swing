    public void GenerarData() throws SQLException, ClassNotFoundException{
        String[] valores = new String[10];
        List<Funcionario> x = ad.listAll();
        for(Funcionario m : x){
            valores[0]=String.valueOf(m.getId());
            valores[1]=m.getTipoIdentificacion();
            valores[2]=m.getNumeroIdentificacion();
            valores[3]= m.getNombres();
            valores[4]= m.getApellidos();
            valores[5] = m.getEstadoCivil();
            valores[6] = m.getSexo();
            valores[7] = m.getDireccion();
            valores[8] = m.getTelefono();
            valores[9] = m.getFechaNacimiento();
            
            tb.addRow(valores);
        }
    }

